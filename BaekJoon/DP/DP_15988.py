mod = 1000000009
dp = [0 for _ in range(1000001)]
dp[1], dp[2], dp[3] = 1, 2, 4
for i in range(4, 1000001):
    dp[i] = dp[i - 1] % mod + dp[i - 2] % mod + dp[i - 3] % mod

for _ in range(int(input())):
    n = int(input())
    print(dp[n] % mod)